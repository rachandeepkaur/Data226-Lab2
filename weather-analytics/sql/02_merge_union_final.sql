-- Reference SQL aligned with the Airflow Python tasks (idempotent load + MERGE/UNION).
-- ETL DAG stages into a temp table then DELETE+INSERT into WEATHER_RAW_DAILY (transaction in Python).

CREATE OR REPLACE TEMP TABLE TMP_WEATHER_RAW_DAILY (
  LOCATION_NAME STRING,
  LATITUDE FLOAT,
  LONGITUDE FLOAT,
  DATE DATE,
  TEMP_MAX FLOAT,
  TEMP_MIN FLOAT,
  TEMP_MEAN FLOAT,
  PRECIP_MM FLOAT
);

-- After INSERT INTO TMP_WEATHER_RAW_DAILY ... (from application), same transaction:
DELETE FROM WEATHER_RAW_DAILY t
USING TMP_WEATHER_RAW_DAILY s
WHERE t.LOCATION_NAME = s.LOCATION_NAME
  AND t.DATE = s.DATE;

INSERT INTO WEATHER_RAW_DAILY
  (LOCATION_NAME, LATITUDE, LONGITUDE, DATE, TEMP_MAX, TEMP_MIN, TEMP_MEAN, PRECIP_MM, INGESTED_AT)
SELECT
  s.LOCATION_NAME, s.LATITUDE, s.LONGITUDE, s.DATE, s.TEMP_MAX, s.TEMP_MIN, s.TEMP_MEAN, s.PRECIP_MM,
  CURRENT_TIMESTAMP()
FROM TMP_WEATHER_RAW_DAILY s;

-- Forecast DAG: temp stage then merge into WEATHER_FORECAST_DAILY.

CREATE OR REPLACE TEMP TABLE TMP_WEATHER_FORECAST_DAILY (
  LOCATION_NAME STRING,
  FORECAST_DATE DATE,
  PREDICTED_TEMP_MAX FLOAT,
  MODEL_NAME STRING,
  TRAIN_START_DATE DATE,
  TRAIN_END_DATE DATE
);

MERGE INTO WEATHER_FORECAST_DAILY t
USING TMP_WEATHER_FORECAST_DAILY s
  ON t.LOCATION_NAME = s.LOCATION_NAME
 AND t.FORECAST_DATE = s.FORECAST_DATE
WHEN MATCHED THEN UPDATE SET
  t.PREDICTED_TEMP_MAX = s.PREDICTED_TEMP_MAX,
  t.MODEL_NAME = s.MODEL_NAME,
  t.TRAIN_START_DATE = s.TRAIN_START_DATE,
  t.TRAIN_END_DATE = s.TRAIN_END_DATE,
  t.CREATED_AT = CURRENT_TIMESTAMP()
WHEN NOT MATCHED THEN INSERT
  (LOCATION_NAME, FORECAST_DATE, PREDICTED_TEMP_MAX, MODEL_NAME, TRAIN_START_DATE, TRAIN_END_DATE)
VALUES
  (s.LOCATION_NAME, s.FORECAST_DATE, s.PREDICTED_TEMP_MAX, s.MODEL_NAME, s.TRAIN_START_DATE, s.TRAIN_END_DATE);

-- Final table: union actuals and forecasts (forecast task truncates then reloads).

TRUNCATE TABLE WEATHER_FINAL_DAILY;

INSERT INTO WEATHER_FINAL_DAILY
  (location_name, date, temp_max, temp_min, temp_mean, is_forecast, model_name)
SELECT
  LOCATION_NAME AS location_name,
  DATE AS date,
  TEMP_MAX AS temp_max,
  TEMP_MIN AS temp_min,
  TEMP_MEAN AS temp_mean,
  FALSE AS is_forecast,
  NULL AS model_name
FROM WEATHER_RAW_DAILY
UNION ALL
SELECT
  LOCATION_NAME AS location_name,
  FORECAST_DATE AS date,
  PREDICTED_TEMP_MAX AS temp_max,
  NULL AS temp_min,
  NULL AS temp_mean,
  TRUE AS is_forecast,
  MODEL_NAME AS model_name
FROM WEATHER_FORECAST_DAILY;
